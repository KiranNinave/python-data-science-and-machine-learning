import numpy as np

arr = np.arange(0, 11)

print("arr", arr)

print("addition", arr + arr)

print("subscration", arr - arr)

print("scalar addition", arr + 100) # scalar means operation with one number

print("log", np.log(arr))

print("sin", np.sin(arr))

print("square", np.power(arr, 2))

print('sqrt', np.sqrt(arr))